def t(key: str, *args) -> str:
    """Return translated string for current language. Use {0}, {1} for format args."""
    s = STRINGS.get(LANG, STRINGS["pl"]).get(key, key)
    if args:
        try:
            return s.format(*args)
        except (IndexError, KeyError):
            return s
    return s


def set_lang(lang: str):
    global LANG
    LANG = lang if lang in ("pl", "en") else LANG
